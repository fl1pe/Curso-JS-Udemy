// Variavéis com let

/*
Variaveis são espaços de memória que armazenam valores.
E sempre que precisamos do conteúdo refereciamos a variavel.
não podemos criar variaveis com palavras reservadas, como var, let, const,console.log, etc.
variaveis podem ter qualquer nome, mas não podem começar com números, nem
ter espaços em branco e precisam ter nome significativo.
Ultilizamos camelCase para nomes com mais de uma palavra.
Variaveis são case sensitive.

NÃO ULTILIZAR VAR, ULTILIZAR LET

Variaveis declaradas com let não podem ser redeclaradas.
 */

let nome; // Declaração de variavel

nome = 'João'; // Atribuição de valor a variavel
console.log(nome);